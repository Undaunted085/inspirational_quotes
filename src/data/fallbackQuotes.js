// Random fallback quote for a mood. Falls back to the whole pool when a mood
// has no local matches, so a quote is always returned.
export function getFallbackQuote(categoryId) {
  const pool = fallbackQuotes.filter((q) => q.category === categoryId)
  const source = pool.length > 0 ? pool : fallbackQuotes
  const pick = source[Math.floor(Math.random() * source.length)]
  return { text: pick.text, author: null }
}
